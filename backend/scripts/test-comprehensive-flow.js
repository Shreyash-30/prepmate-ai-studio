#!/usr/bin/env node
/**
 * COMPREHENSIVE TEST: ProblemId Generation Consistency
 * 
 * Verifies that:
 * 1. AI service generates problemIds using slugified titles
 * 2. Backend receives questions with problemIds
 * 3. Questions are saved to database with those IDs
 * 4. Sessions can be created with those IDs
 * 
 * Test Flow:
 * Generate "two-sum" → Should create problemId "two-sum"
 * Try to start session with problemId "two-sum" → Should succeed
 */

const axios = require('axios');
const mongoose = require('mongoose');
const path = require('path');
require('dotenv').config({ path: path.join(__dirname, '../.env') });

const { Question: QuestionBank } = require('../src/models');
const User = require('../src/models/User');

const BACKEND_URL = 'http://localhost:5000/api';
const MONGODB_URI = process.env.MONGODB_URI || 'mongodb://localhost:27017/prepmate';
const TOPIC = 'two-sum';

async function test() {
  console.log(`\n${'='.repeat(70)}`);
  console.log(`COMPREHENSIVE TEST: ProblemId Consistency`);
  console.log(`${'='.repeat(70)}\n`);

  let generatedQuestions = [];
  let sessionCreated = false;

  try {
    // 1. Connect to MongoDB
    console.log(`[1/5] Connecting to MongoDB...`);
    await mongoose.connect(MONGODB_URI);
    console.log(`✅ Connected\n`);

    // 2. Create test user
    console.log(`[2/5] Setting up test user...`);
    let user = await User.findOne();
    if (!user) {
      user = await User.create({
        googleId: `test-${Date.now()}`,
        email: `test${Date.now()}@example.com`,
        name: 'Test User',
      });
    }
    const userId = user._id;
    console.log(`✅ Using user: ${userId}\n`);

    // 3. Generate questions via backend
    console.log(`[3/5] Calling backend to generate questions...`);
    console.log(`   URL: POST ${BACKEND_URL}/practice/topics/${TOPIC}/generate-questions`);
    console.log(`   Topic: ${TOPIC}\n`);

    const genResponse = await axios.post(
      `${BACKEND_URL}/practice/topics/${TOPIC}/generate-questions`,
      { userId, limit: 2 },
      { headers: { 'Content-Type': 'application/json' }, timeout: 30000 }
    );

    generatedQuestions = genResponse.data?.data?.questions || [];
    console.log(`✅ Backend returned ${generatedQuestions.length} questions\n`);

    if (generatedQuestions.length === 0) {
      throw new Error('No questions generated by backend!');
    }

    // 4. Verify questions have problemIds and are in database
    console.log(`[4/5] Verifying questions are saved in database...\n`);

    for (let i = 0; i < Math.min(2, generatedQuestions.length); i++) {
      const q = generatedQuestions[i];
      console.log(`   Question ${i + 1}: ${q.problemTitle || 'Unknown'}`);

      if (!q.problemId) {
        console.log(`      ❌ MISSING problemId!`);
        throw new Error(`Question ${i + 1} missing problemId!`);
      }

      console.log(`      problemId: ${q.problemId}`);
      console.log(`      schemaVersion: ${q.schemaVersion}`);

      // Check if it's in the database
      const inDb = await QuestionBank.findOne({ problemId: q.problemId });
      if (inDb) {
        console.log(`      ✅ Found in database`);
      } else {
        console.log(`      ❌ NOT FOUND in database`);
        // List what IS in the database for this title
        const similar = await QuestionBank.find({ title: q.problemTitle }).lean();
        console.log(`         Questions with title "${q.problemTitle}" in DB:`, similar.length);
        similar.slice(0, 2).forEach(s => {
          console.log(`         - problemId: ${s.problemId}, schemaVersion: ${s.schemaVersion}, isActive: ${s.isActive}`);
        });
        throw new Error(`Cannot find generated question in database!`);
      }
    }
    console.log('');

    // 5. Try to start a session with the first question
    console.log(`[5/5] Testing session creation...\n`);
    const firstQ = generatedQuestions[0];
    console.log(`   problemId: ${firstQ.problemId}`);
    console.log(`   topicId: ${TOPIC}\n`);

    const sessionResponse = await axios.post(
      `${BACKEND_URL}/practice/session/start`,
      {
        problemId: firstQ.problemId,
        topicId: TOPIC,
        language: 'python',
        userId,
      },
      { headers: { 'Content-Type': 'application/json' }, timeout: 10000 }
    );

    if (sessionResponse.data?.success) {
      sessionCreated = true;
      console.log(`✅ Session created successfully!`);
      console.log(`   sessionId: ${sessionResponse.data.data?.sessionId}`);
  console.log(`   schemaVersion: ${sessionResponse.data.data?.schemaVersion}\n`);
    } else {
      throw new Error(`Session creation returned: ${JSON.stringify(sessionResponse.data)}`);
    }

    // Success!
    console.log(`${'='.repeat(70)}`);
    console.log(`✅ ALL TESTS PASSED`);
    console.log(`${'='.repeat(70)}`);
    console.log(`Generated: ${generatedQuestions.length} questions`);
    console.log(`Saved in DB: ✅ All found`);
    console.log(`Session created: ✅ Working`);
    console.log(`ProblemId consistency: ✅ Verified\n`);
  } catch (error) {
    console.log(`\n${'='.repeat(70)}`);
    console.log(`❌ TEST FAILED`);
    console.log(`${'='.repeat(70)}`);
    console.log(`Error: ${error.message}\n`);

    if (error.response?.status === 500) {
      console.log(`Backend responded with 500:`);
      console.log(`  ${error.response.data?.message || error.response.data?.error}\n`);
    }

    console.log(`Generated Questions:`);
    generatedQuestions.forEach((q, i) => {
      console.log(`  [${i + 1}] ${q.problemTitle} (problemId: ${q.problemId || 'MISSING'})`);
    });

    process.exit(1);
  } finally {
    await mongoose.connection.close();
  }
}

test();
